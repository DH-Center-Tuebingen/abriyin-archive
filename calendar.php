<?php 
	//------------------------------------------------------------------------------------------    
	function gregorian_to_hijri($y, $m, $d) {
	//------------------------------------------------------------------------------------------
        return jd_to_hijri(cal_to_jd(CAL_GREGORIAN, $m, $d, $y));
    }

	//------------------------------------------------------------------------------------------		
    function hijri_to_gregorian($y, $m, $d)  {
	//------------------------------------------------------------------------------------------
		/* Return value is in this format:		
			Array
			(
				[date] => 11/4/1969
				[month] => 11
				[day] => 4
				[year] => 1969
				[dow] => 2
				[abbrevdayname] => Tue
				[dayname] => Tuesday
				[abbrevmonth] => Nov
				[monthname] => November
			)		
		*/	
        return cal_from_jd(hijri_to_jd($m, $d, $y), CAL_GREGORIAN);
    }

	//------------------------------------------------------------------------------------------
    function jd_to_hijri($jd) {
	//------------------------------------------------------------------------------------------
        $jd = $jd - 1948440 + 10632;
        $n  = (int)(($jd - 1) / 10631);
        $jd = $jd - 10631 * $n + 354;
        $j  = ((int)((10985 - $jd) / 5316)) *
            ((int)(50 * $jd / 17719)) +
            ((int)($jd / 5670)) *
            ((int)(43 * $jd / 15238));
        $jd = $jd - ((int)((30 - $j) / 15)) *
            ((int)((17719 * $j) / 50)) -
            ((int)($j / 16)) *
            ((int)((15238 * $j) / 43)) + 29;
        $m  = (int)(24 * $jd / 709);
        $d  = $jd - (int)(709 * $m / 24);
        $y  = 30*$n + $j - 30;

        return array($m, $d, $y);
    }

	//------------------------------------------------------------------------------------------
    function hijri_to_jd($m, $d, $y) {
	//------------------------------------------------------------------------------------------    
        return (int)((11 * $y + 3) / 30) +
            354 * $y + 30 * $m -
            (int)(($m - 1) / 2) + $d + 1948440 - 385;
    }
